public class ngay {
    private int day;
    private int month;
    private int year;

    public ngay(int d, int m, int y) {
        this.day = d;
        this.month = m;
        this.year = y;
    }
    public int getDay() {
        return this.day;
    }
    public void setDay(int day) {
        this.day = day;
    }
    public int getMonth() {
        return this.month;
    }
    public void setMonth(int month) {
        this.month = month;
    }
    public int getYear() {
        return this.year;
    }
    public void setYear(int year) {
        this.year = year;
    }
}
